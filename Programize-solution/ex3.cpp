// Program to Find Size of int, float, double and char in Your System

#include <iostream>
using namespace std;

int main(){
	int integer_size;
	float float_size;
	double double_size;
	char char_size;
	
	cout<<"Size of integer "<<sizeof(integer_size)<<endl;
	cout<<"Size of float "<<sizeof(float_size)<<endl;
	cout<<"Size of double "<<sizeof(double_size)<<endl;
	cout<<"Size of char "<<sizeof(char_size)<<endl;	
	
}
